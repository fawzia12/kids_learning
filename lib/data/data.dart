  switch (index % 7) {
    case 0:
      return 0.5;
    case 1:
      return 0.3;
    case 2:
      return 0.7;
    case 3:
      return 0.3;
    case 4:
      return 0.7;
    case 5:
      return 0.5;
    default:
      return 0.3;
  }
}
